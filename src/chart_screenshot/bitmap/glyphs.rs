pub(super) const BITMAP_GLYPH_WIDTH: u32 = 5;
pub(crate) const BITMAP_GLYPH_HEIGHT: u32 = 7;

pub(crate) fn bitmap_text_width(text: &str, scale: u32) -> u32 {
    let count = text.chars().count() as u32;
    if count == 0 {
        0
    } else {
        ((BITMAP_GLYPH_WIDTH + 1) * count - 1) * scale
    }
}

pub(crate) fn bitmap_max_chars(available_width: u32, scale: u32) -> usize {
    ((available_width / ((BITMAP_GLYPH_WIDTH + 1) * scale)).max(1)) as usize
}

pub(crate) fn is_bitmap_glyph_supported(ch: char) -> bool {
    matches!(
        ch,
        'A'..='Z'
            | '0'..='9'
            | '/'
            | ':'
            | '-'
            | '_'
            | '.'
            | ','
            | '+'
            | '$'
            | '%'
            | '*'
            | ' '
    )
}

